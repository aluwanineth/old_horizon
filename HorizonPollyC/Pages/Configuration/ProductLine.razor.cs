using HorizonPollyC.Models.Configuration;
using HorizonPollyC.Services.Configuration;
using HorizonPollyC.Services.General;
using Microsoft.AspNetCore.Components;
using Microsoft.JSInterop;
using Radzen;
using Radzen.Blazor;


namespace HorizonPollyC.Pages.Configuration
{
    partial class ProductLine
    {

        RadzenDataGrid<ProductLineVM> modelGrid = null;
        ProductLineVM modelToInsert = null;
        public IEnumerable<ProductLineVM> modelList = new List<ProductLineVM>();
        bool enable = true;

        protected override async Task OnInitializedAsync()
        {
            modelList = await _genericService.Get();
        }

        public async Task Export(string type)
        {
            await _exportService.ExportData<ProductLineVM>(modelGrid, type, "ProductLine", "ProductLine");
        }
        async Task EditRow(ProductLineVM pModel)
        {
            await modelGrid.EditRow(pModel);
        }

        async void OnUpdateRow(ProductLineVM pModel)
        {
            if (pModel == modelToInsert)
            {
                modelToInsert = null;
            }

            await _genericService.Update(pModel);
        }

        async Task SaveRow(ProductLineVM pModel)
        {
            if (pModel == modelToInsert)
            {
                modelToInsert = null;
            }

            await modelGrid.UpdateRow(pModel);
        }

        void CancelEdit(ProductLineVM pModel)
        {
            if (pModel == modelToInsert)
            {
                modelToInsert = null;
            }
            modelGrid.CancelEditRow(pModel);

        }

        async Task DeleteRow(ProductLineVM pModel)
        {
            if (pModel == modelToInsert)
            {
                modelToInsert = null;
            }

            if (modelList.Contains(pModel))
            {
                modelList.ToList().Remove(pModel);
                await modelGrid.Reload();
            }
            else
            {
                modelGrid.CancelEditRow(pModel);
            }
        }



        async Task InsertRow()
        {
            enable = false;
            modelToInsert = new ProductLineVM();
            await modelGrid.InsertRow(modelToInsert);

        }

        async Task OnCreateRow(ProductLineVM pModel)
        {
            await _genericService.Update(pModel);
        }
    }

}
